package com.cbt.entity;

import java.io.Serializable;

/**
* @ClassName: ClientDrawings 
* @Description: 客户图纸信息
 */
public class InvoiceProduct implements Serializable{
	

    /**
	 * 发票产品信息
	 */
	private static final long serialVersionUID = 1L;
	
	private Integer id;
    private String invoiceId; //发票编号
	private Double unitPrice;//产品单价
	private Integer quantity;//产品数量
	private String productName; //产品名称
	private Double moldPrice;  //模板价格
	
	
	
	public Double getMoldPrice() {
		return moldPrice;
	}
	public void setMoldPrice(Double moldPrice) {
		this.moldPrice = moldPrice;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getInvoiceId() {
		return invoiceId;
	}
	public void setInvoiceId(String invoiceId) {
		this.invoiceId = invoiceId;
	}

	public Double getUnitPrice() {
		return unitPrice;
	}
	public void setUnitPrice(Double unitPrice) {
		this.unitPrice = unitPrice;
	}
	public Integer getQuantity() {
		return quantity;
	}
	public void setQuantity(Integer quantity) {
		this.quantity = quantity;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	@Override
	public String toString() {
		return "InvoiceProduct [id=" + id + ", invoiceId=" + invoiceId
				+ ", unitPrice=" + unitPrice + ", quantity=" + quantity
				+ ", productName=" + productName + ", moldPrice=" + moldPrice
				+ "]";
	}

	
	
	

	
	



	
	
	
}
