package com.cbt.entity;

import java.io.Serializable;

public class Milestone implements Serializable {
	  

	  
			private Integer id;
			private String orderId;   //订单编号
			private String milestoneDate;    //里程碑时间
			private String milestoneName;   //里程碑名称
			private Integer delayStatus;    //是否延期(0 未延期 1 延期 2 完成)
			private String delayDate;       //延期时间
			private String delayRemark;     //延期备注
			private Integer productionPhotoStatus;  //是否上传图片(0 未上传 1 已上传)
			private String createTime;              //里程碑创建时间
			private String photoesRemark;           //图片
			private Integer expectedOrActually;      //(0 预计1实际)
			
			
			
			public Integer getExpectedOrActually() {
				return expectedOrActually;
			}
			public void setExpectedOrActually(Integer expectedOrActually) {
				this.expectedOrActually = expectedOrActually;
			}
			public String getPhotoesRemark() {
				return photoesRemark;
			}
			public void setPhotoesRemark(String photoesRemark) {
				this.photoesRemark = photoesRemark;
			}
			public Integer getId() {
				return id;
			}
			public void setId(Integer id) {
				this.id = id;
			}
			public String getOrderId() {
				return orderId;
			}
			public void setOrderId(String orderId) {
				this.orderId = orderId;
			}
			public String getMilestoneDate() {
				return milestoneDate;
			}
			public void setMilestoneDate(String milestoneDate) {
				this.milestoneDate = milestoneDate;
			}
			public String getMilestoneName() {
				return milestoneName;
			}
			public void setMilestoneName(String milestoneName) {
				this.milestoneName = milestoneName;
			}
			public Integer getDelayStatus() {
				return delayStatus;
			}
			public void setDelayStatus(Integer delayStatus) {
				this.delayStatus = delayStatus;
			}
			public String getDelayDate() {
				return delayDate;
			}
			public void setDelayDate(String delayDate) {
				this.delayDate = delayDate;
			}
			public String getDelayRemark() {
				return delayRemark;
			}
			public void setDelayRemark(String delayRemark) {
				this.delayRemark = delayRemark;
			}
			public Integer getProductionPhotoStatus() {
				return productionPhotoStatus;
			}
			public void setProductionPhotoStatus(Integer productionPhotoStatus) {
				this.productionPhotoStatus = productionPhotoStatus;
			}
			public String getCreateTime() {
				return createTime;
			}
			public void setCreateTime(String createTime) {
				this.createTime = createTime;
			}
			@Override
			public String toString() {
				return "Milestone [id=" + id + ", orderId=" + orderId
						+ ", milestoneDate=" + milestoneDate
						+ ", milestoneName=" + milestoneName + ", delayStatus="
						+ delayStatus + ", delayDate=" + delayDate
						+ ", delayRemark=" + delayRemark
						+ ", productionPhotoStatus=" + productionPhotoStatus
						+ ", createTime=" + createTime + ", photoesRemark="
						+ photoesRemark + ", expectedOrActually="
						+ expectedOrActually + "]";
			}

			
			
		
			
	
}
