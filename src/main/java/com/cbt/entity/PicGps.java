package com.cbt.entity;

public class PicGps {

	public PicGps(int startRow, int startCol, int stopRow, int stopCol,
			int startX, int startY, int stopX, int stopY) {
		this.startRow = startRow;
		this.startCol = startCol;
		this.stopRow = stopRow;
		this.stopCol = stopCol;
		this.startX = startX;
		this.startY = startY;
		this.stopX = stopX;
		this.stopY = stopY;

	}

	private int startRow;
	private int startCol;
	private int stopRow;
	private int stopCol;
	private int startX;
	private int startY;
	private int stopX;
	private int stopY;

	public int getStartRow() {
		return startRow;
	}

	public void setStartRow(int startRow) {
		this.startRow = startRow;
	}

	public int getStartCol() {
		return startCol;
	}

	public void setStartCol(int startCol) {
		this.startCol = startCol;
	}

	public int getStopRow() {
		return stopRow;
	}

	public void setStopRow(int stopRow) {
		this.stopRow = stopRow;
	}

	public int getStopCol() {
		return stopCol;
	}

	public void setStopCol(int stopCol) {
		this.stopCol = stopCol;
	}

	public int getStartX() {
		return startX;
	}

	public void setStartX(int startX) {
		this.startX = startX;
	}

	public int getStartY() {
		return startY;
	}

	public void setStartY(int startY) {
		this.startY = startY;
	}

	public int getStopX() {
		return stopX;
	}

	public void setStopX(int stopX) {
		this.stopX = stopX;
	}

	public int getStopY() {
		return stopY;
	}

	public void setStopY(int stopY) {
		this.stopY = stopY;
	}

}
